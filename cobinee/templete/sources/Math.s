; sin の値を取得する
;
_MathGetSin::
    
    ; レジスタの保存
    push    bc
    push    ix
    
    ; 値の取得
    ld      c, a
    ld      b, #0x00
    ld      ix, #trigonometricDecimalTable
    add     ix, bc
    ld      l, 0(ix)
    ld      ix, #trigonometricIntegerTable
    add     ix, bc
    ld      h, 0(ix)
    
    ; レジスタの復帰
    pop     ix
    pop     bc
    
    ; 終了
    ret

; cos の値を取得する
;
_MathGetCos::
    
    ; レジスタの保存
    push    af
    push    bc
    push    ix
    
    ; 値の取得
    add     a, #0x40
    ld      c, a
    ld      b, #0x00
    ld      ix, #trigonometricDecimalTable
    add     ix, bc
    ld      l, 0(ix)
    ld      ix, #trigonometricIntegerTable
    add     ix, bc
    ld      h, 0(ix)
    
    ; レジスタの復帰
    pop     ix
    pop     bc
    pop     af
    
    ; 終了
    ret

; atan2 の値を取得する
;
_MathGetAtan2::
    
    ; レジスタの保存
    push    hl
    push    bc
    push    de
    
    ; 値の取得
    ld      c, #0x00
    ld      a, l
    sla     l
    rl      c
    bit     #0x00, c
    jr      z, 10$
    neg
    jp      po, 10$
    ld      a, #0x7f
10$:
    ld      l, a
    ld      a, h
    sla     h
    rl      c
    bit     #0x00, c
    jr      z, 11$
    neg
    jp      po, 11$
    ld      a, #0x7f
11$:
    ld      h, a
    cp      l
    jr      nc, 12$
    ld      a, l
12$:
    cp      #0x08
    jr      c, 13$
    sra     a
    sra     h
    sra     l
    jr      12$
13$:
    ld      a, l
    sla     a
    sla     a
    sla     a
    add     a, h
    ld      e, a
    ld      d, #0x00
    ld      hl, #trigonometricAtanAngleTable
    add     hl, de
    ld      a, (hl)
    ld      b, c
    inc     b
    bit     #0x01, b
    jr      z, 14$
    neg
14$:
    ld      b, #0x00
    ld      hl, #trigonometricAtanOffsetTable
    add     hl, bc
    add     a, (hl)
    
    ; レジスタの復帰
    pop     de
    pop     bc
    pop     hl
    
    ; 終了
    ret

